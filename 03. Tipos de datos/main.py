# Enteros - int
x = 60
print("x vale", x ,"y es de tipo:", type(x))

# Decimales - float
x = 3.14
print("x vale", x ,"y es de tipo:", type(x))

# Booleanos - bool
x = True
print("x vale", x ,"y es de tipo:", type(x))

# Strings (cadenas de caracteres) - str
x = "Hola, Mundo!"
print("x vale", x ,"y es de tipo:", type(x))

# Tuplas - tuple
x = (10, 20, 30)
print("x vale", x ,"y es de tipo:", type(x))

# Listas - list
x =  [40, 50, 60]
print("x vale", x ,"y es de tipo:", type(x))

# Diccionarios - dict
x = {
    "marca": "Ford",
    "modelo": "Mustang",
    "año": 1964
}
print("x vale", x ,"y es de tipo:", type(x))

x = None
print("x vale", x ,"y es de tipo:", type(x))

# Imprime:
# x vale 60 y es de tipo: <class 'int'>
# x vale 3.14 y es de tipo: <class 'float'>
# x vale True y es de tipo: <class 'bool'>
# x vale Hola, Mundo! y es de tipo: <class 'str'>
# x vale (10, 20, 30) y es de tipo: <class 'tuple'>
# x vale [40, 50, 60] y es de tipo: <class 'list'>
# x vale {'año': 1964, 'marca': 'Ford', 'modelo': 'Mustang'}
#     y es de tipo: <class 'dict'>
# x vale None y es de tipo: <class 'NoneType'>